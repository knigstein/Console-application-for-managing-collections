package command;

/**
 * Интерфейс команды.
 * Определяет общий контракт для всех команд консольного приложения.
 * Каждая команда должна уметь выполняться с набором строковых аргументов
 * и предоставлять краткое текстовое описание для справки.
 *
 * Данный интерфейс реализуется всеми конкретными командами в пакете {@code command}.
 */

public interface Command {

    /**
     * Выполняет логику команды.
     *
     * @param args аргументы команды, переданные пользователем
     */

    void execute(String[] args);

    /**
     * Возвращает краткое текстовое описание команды.
     * Используется при выводе справки по доступным командам.
     *
     * @return строка с описанием назначения команды
     */

    String getDescription();

}
